int check_rank() {
	int rank = 0;
	/*
		1	탑 : 숫자가 이어지지 않고 같은 숫자가 없는 경우
		2	원페어 : 똑같은 숫자가 2개인 경우 (= K탑)
		3	투페어 : 똑같은 숫자인 2개 + 똑같은 숫자인 2개 o
		4	트리플 : 똑같은 숫자 3개 o
		5	풀하우스 : 똑같은 숫자2개 + 똑같은 숫자 3개 o
		6	스트레이트 : 이어지는 숫자 5개 o
		7	포카드 : 똑같은 숫자 4개 o
		8	플러쉬 : 똑같은 색깔 5개 o
		9	스트레이트 플러쉬 : 이어지는 숫자, 같은 색깔 5개 o
		10	로얄 스트레이트 플러쉬 : 10, J, Q, K, A, 같은 색 o
	*/

	for (int i = 0; i < 5; i++) {
		card_num[cards[i][0]]++;
		card_color[cards[i][1]]++;
	}

	// 0 , 1,  2,  3,    4
	// 0개 1개, 2개, 3개,4개
	int num_cnt[5] = { 0, };
	for (int i = 0; i < 13; i++)
		num_cnt[card_num[i]]++;

	if (num_cnt[1] > 0)
		rank = 1;
	for (int i = 0; i < 5; i++)
		if (cards[i][0] == 11)
			rank = 2;
	if (num_cnt[2] > 0)
		rank = 2;
	if (num_cnt[2] > 1)
		rank = 3;
	if (num_cnt[3] > 0)
		rank = 4;
	if (num_cnt[2] > 0 && num_cnt[3] > 0)
		rank = 5;
	if (check_straight() != 0)
		rank = 6;
	if (num_cnt[4] > 0)
		rank = 7;

	for (int i = 0; i < 4; i++)
		if (card_color[i] == 5) {
			rank = 8;
			if (check_straight() != 0)
				rank = 9;
			if (check_straight() == 9)
				rank = 10;
		}
	return rank;
}