int poker() {
	poker_init();

	int dack[13 * 4];
	int dack_size = 13 * 4;
	srand(time(NULL));

	for (int i = 0; i < 13 * 4; i++)
		dack[i] = i;

	for (int i = 0; i < 5; i++) {
		int dack_num = rand() % (dack_size--);
		cards[i][0] = dack[dack_num] / 4;
		cards[i][1] = dack[dack_num] % 4;
		cards[i][2] = 1;
		for (int j = dack_num; j < 13 * 4 - 1; j++)
			dack[j] = dack[j + 1];
	}

	//choose card lists
	int first_while_cnt = 0;
	char num = '0'-1;
	while (1) {
		int cnt = 0;
		for (int i = 0; i < 5; i++)
			if (cards[i][2] == 0)
				cnt++;

		if (cnt == 5) {
			cout << "\nfinished\n";
			break;
		}

		for (int i = 0; i < 5; i++)
			print_card(i);

		gotoxy(CARDPOINTX, 1);
		printf("Select change cards number!");

		int key;
		int tal = 0;
		int while_cnt = 1;

		num++;
		while (1) {
			print_select(num - '0');
			key = _getch();
			if (key == 97) {    //left (a)
				if (num == '1') {
					num = '5';
					continue;
				}
				if (num == '0') {
					num = '5';
					continue;
				}
				num--;
			}
			if (key == 100) {    //right (d)
				if (num == '5') {
					num = '1';
					continue;
				}
				if (num == '0') {
					num = '5';
					continue;
				}
				num++;
			}
			if (key == 'w') {
				num = '0';
			}
			if (key == 's') {
				num = '0';
			}
			if (key == 32) {		//finished space
				break;
			}
		}
		print_select(num - '0');

		num--;
		// jokbo tesk code
		if (num == (']' - 1)) {
			for (int i = 0; i < 5; i++) {
				cards[i][1] = 0;
				cards[i][2] = 0;
				cards[i][0] = i + 9;
				if (i == 4)
					cards[i][0] = 8;
			}
		}

		if (num == ('0' - 1))
			break;
		if (num < '0' || num > '5')
			continue;
		else {
			if (cards[num - '0'][2] == 0) {
				continue;
			}
			int dack_num = rand() % (dack_size--);
			cards[num - '0'][0] = dack[dack_num] / 4;
			cards[num - '0'][1] = dack[dack_num] % 4;
			cards[num - '0'][2]--;
			for (int j = dack_num; j < 13 * 4 - 1; j++)
				dack[j] = dack[j + 1];
		}
	}

	//card rank check
	int rank = check_rank();

	for (int i = 0; i < 5; i++)
		print_card(i);
	gotoxy(CARDPOINTX, 1);
	printf("        rank : %d / %s", rank, string_rank[rank]);
	print_select(0);

	return rank;
}

